public class Employee {
    Employee(int d, String ka) {
        this.d = d;
        this.ka = ka;
    }

    private int d;

    public int getD() {
        return d;
    }

    public void setD(int d) {
        this.d = d;
    }

    public String getKa() {
        return ka;
    }

    public void setKa(String ka) {
        this.ka = ka;
    }

    private String ka;
}
